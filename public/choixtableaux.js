const lucrecePainting = document.getElementById('Lucrece');
const modalLucrece = document.getElementById('modalLucrece');
const closeLucrece = document.getElementById('closeLucrece');

lucrecePainting.addEventListener('click', () => {
    modalLucrece.classList.add('opened');
})

closeLucrece.addEventListener('click', () => {
    modalLucrece.classList.remove('opened');
})


